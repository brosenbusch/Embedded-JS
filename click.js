//declare samecolor() here
function samecolor(){
  let doc = document;
  let divs = doc.querySelectorAll("#samecolor div");

  divs[0].style.backgroundColor = rgb(100,200,50);
  divs[1].style.backgroundColor = rgb(100,200,50);
  divs[2].style.backgroundColor = rgb(100,200,50);
  divs[3].style.backgroundColor = rgb(100,200,50);

  divs[0].style.opacity = 0.2;
  divs[1].style.opacity = 0.4;
  divs[2].style.opacity = 0.8;
  divs[3].style.opacity = 1;
}
